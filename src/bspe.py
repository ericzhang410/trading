#Assumptions of BSM
# 1. Proceeds from short sale can be reinvested at same interest rate.
# 2. The underlying is tradable, at any size necessary without transaction costs
# 3. Changes in the underlying are continuous and smooth.

#Delta-hedging:
# - Delta refers to the instantaneous change in the option over the change of the price. (The deritive dV/dS where V is the option, S is the underlying)
# - Delta hedging is minimizing portfolio delta as to minimize volatility risk

#Gamma - The second derivative of dV/dS.

#Vega - The derivative of option over the IV

#Theta - the derivative of option price over time